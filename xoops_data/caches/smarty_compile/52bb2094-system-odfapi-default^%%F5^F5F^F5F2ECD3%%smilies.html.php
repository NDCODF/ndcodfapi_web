<h1 class="head">協助主題：表情圖</h1>

<h4 class="odd">說明</h4>
<p class="even">
表情圖是一些可以用來表達心情的小圖示，他們可以用來取代文章中、評論中的表情符號。XOOPS內建了17種表情圖，他們會顯示在內建的編輯器中（包括 forums, news, downloads, links 等模組）。只要點擊該圖示，他們就會自動的插入到文章之中，或者您也可以直接輸入他們所屬的表情符號代碼，例如：「:-(」或「:cry:」之類的，送出後，他們會自動被取代為表情圖片。
<br><br>
表情圖的完整列表可以透過後台的「表情圖」功能來查看。在這裡，您可以自行新增或刪除表情圖及其代碼，並可決定哪些表情圖要出現於表單之中（預設狀態17個中只有9個會出現在表單中）
<br><br>
假如您不要使用某個表情圖，您可以在表單中取消勾選「顯示於表單中」，這樣子就不會出現在前台表單中，但是使用者一樣可以透過「代碼」來使用該表情圖。當然您也可以直接刪除該圖片，這樣就真的沒辦法使用該圖片了。
</p>

