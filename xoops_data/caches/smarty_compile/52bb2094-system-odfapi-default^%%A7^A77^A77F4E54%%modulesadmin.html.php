<h4 class="odd">說明</h4>
<p class="even">
模組是XOOPS用來新增網站功能或內容的一種軟體架構，您可以透過XOOPS的界面新增或管理之。<br><br>
網站管理員可以輕鬆的從後臺來安裝、移除、啟動、關閉任何一個模組（系統模組除外，移掉網站就掛了）。您也可以修改模組名稱並決定模組在主選單上出現的先後順序，當然也可以隱藏模組，讓它不出現在主選單上。舉例來說，您可以將一個模組設為隱藏，若要給部份人使用，您可以發訊息告訴他們，直接用連結的方式來使用那些被隱藏的模組，例如：<em>http://www.yoursite.com/modules/news</em>。<br><br>
至於模組順序的排列，您可以直接用拖曳的方式來改變其順序！<br><br>
若想了解一個模組安裝起來之後，看起來會是怎樣？有什麼樣的作用，或是該模組的實際名稱為何時，您可以參考一下別人的XOOPS網站，並且觀摩一下該模組實際運用的狀況即可。您只要從左右兩側的主選單點選一下該模組，然後看一下網址，通常您就可以得到該模組的實際名稱，例如：「http://the_site.com/modules/news/.... 」，該模組名稱就是modules後面的「news」，然後您就可以根據此模組名稱去搜尋相關的資源。<br><br>
此外，當您點進該模組頁面，您就可以看到該模組看起來是怎樣，並了解其大致功能。這對於您去了解一個模組相當有幫助！
