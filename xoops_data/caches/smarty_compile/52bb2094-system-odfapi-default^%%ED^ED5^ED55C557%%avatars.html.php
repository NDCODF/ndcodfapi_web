<h1 class="head">協助主題：頭像</h1>
<h4 class="odd">說明</h4>

<p class="even">
所謂「頭像」是用來辨識使用者的小圖片，頭像可能會出現在網站的任何地方，例如討論區就常用到，頭像通常緊鄰著使用者的姓名。<br><br>

「頭像管理」允許網站管理員去新增、編輯或刪除系統內建頭像以及會員自訂頭像。 <br><br>
</p>
<h4 class="odd">系統內建頭像 </h4>
<p class="even">
所謂「系統內建頭像」指的是系統管理員於「頭像管理」界面所上傳的頭像圖片。
</p>
<h4 class="odd">會員自訂頭像</h4>
<p class="even">
所謂「會員自訂頭像」指的是由使用者直接上傳的頭像。為了讓使用者可以上傳頭像，管理者必須先到後台的「偏好設定→系統設定→註冊會員設定」來設定相關的選項。
</p>

<h4 class="odd">編輯與刪除頭像</h4>
<p class="even">
網站管理員可以編輯所有的頭像名稱以及出現順序，也可以刪除任何的頭像圖片。要注意的是，假如管理員刪除了一個有人使用的頭像，該使用者將看不到他的頭像圖片。
</p>


